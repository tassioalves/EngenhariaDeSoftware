    public void inicializarObjectTableModel() throws SQLException {
        List<ContasReceber> contas = contasReceberDAO.listarTodos();

        String cabecalho = "id,nome,descricao,dataInicio,dataVencimento,valor";

        //tabelaContas.setModel(new DefaultTableModel(header, contas.size()));  // cabeÃ§alho, qtde linhas
        resolver = new AnnotationResolver(ContasReceber.class);
        objectTableModel = new ObjectTableModel(resolver, cabecalho);
        objectTableModel.setData(contas);
        tabelaContas.setModel(objectTableModel);
    }

